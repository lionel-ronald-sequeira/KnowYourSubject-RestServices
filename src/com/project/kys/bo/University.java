package com.project.kys.bo;

public class University {
	
	private Integer universityId=0;
	private String universityName;
	private String universityInitials;
	private String universityCity;
	private String universityState;
	private String universityCountry;
	private String universitySummary;
	
	public Integer getUniversityId() {
		return universityId;
	}
	public String getUniversityName() {
		return universityName;
	}
	public void setUniversityId(Integer universityId) {
		this.universityId = universityId;
	}
	public String getUniversityInitials() {
		return universityInitials;
	}
	public String getUniversityCity() {
		return universityCity;
	}
	public String getUniversityState() {
		return universityState;
	}
	public String getUniversityCountry() {
		return universityCountry;
	}
	public String getUniversitySummary() {
		return universitySummary;
	}
	
	public void setUniversityName(String universityName) {
		this.universityName = universityName;
	}
	public void setUniversityInitials(String universityInitials) {
		this.universityInitials = universityInitials;
	}
	public void setUniversityCity(String universityCity) {
		this.universityCity = universityCity;
	}
	public void setUniversityState(String universityState) {
		this.universityState = universityState;
	}
	public void setUniversityCountry(String universityCountry) {
		this.universityCountry = universityCountry;
	}
	public void setUniversitySummary(String universitySummary) {
		this.universitySummary = universitySummary;
	}
	
	
	
	
}
